    _TEXT_

//****************************************************************************
//
// Compare two 64-bit values to see if they are not equal.
//
//****************************************************************************
    _EXPORT_ _ll_cmpne
_ll_cmpne _LABEL_
    subs    r0, r0, r2
    cmpeq   r1, r3
    movne   r0, _CONST_ 1
    mov     pc, r14

    _END_
