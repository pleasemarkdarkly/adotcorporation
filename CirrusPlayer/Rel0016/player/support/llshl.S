    _TEXT_

//****************************************************************************
//
// Shift left a signed 64-bit value.
//
//****************************************************************************
    _EXPORT_ _ll_shift_l
_ll_shift_l _LABEL_
    subs    r3, r2, _CONST_ 32
    bpl     _ll_shift_l01
    rsb     r3, r2, _CONST_ 32
    mov     r1, r1, lsl r2
    orr     r1, r1, r0, lsr r3
    mov     r0, r0, lsl r2
    mov     pc, r14
_ll_shift_l01 _LABEL_
    mov     r1, r0, lsl r3
    mov     r0, _CONST_ 0
    mov     pc, r14

    _END_
