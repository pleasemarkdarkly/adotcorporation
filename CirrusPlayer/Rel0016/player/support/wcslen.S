    _TEXT_

//****************************************************************************
//
// Unicode version of "strlen".
//
//****************************************************************************
    _EXPORT_ wcslen
wcslen _LABEL_
    mov     r2, _CONST_ 0
wcslen0 _LABEL_
    ldrh    r1, [r0], _CONST_ 2
    cmp     r1, _CONST_ 0
    addne   r2, r2, _CONST_ 1
    bne     wcslen0
    mov     r0, r2
    mov     pc, r14

    _END_
