//****************************************************************************
//
// Compare two 64-bit values to see if they are equal.
//
//****************************************************************************
    _EXPORT_ _ll_cmpeq
_ll_cmpeq _LABEL_
    cmp     r1, r3
    cmpeq   r0, r2
    moveq   r0, _CONST_ 1
    movne   r0, _CONST_ 0
    mov     pc, r14

    _END_
