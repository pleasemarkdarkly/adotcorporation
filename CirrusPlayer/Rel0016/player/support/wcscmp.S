    _TEXT_

//****************************************************************************
//
// Unicode version of "strcmp".
//
//****************************************************************************
    _EXPORT_ wcscmp
wcscmp _LABEL_
    ldrh    r2, [r0], _CONST_ 2
    ldrh    r3, [r1], _CONST_ 2
    cmp     r2, _CONST_ 1
    cmpcs   r2, r3
    beq     wcscmp
    sub     r0, r2, r3
    mov     pc, r14

    _END_
